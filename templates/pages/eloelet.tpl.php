<pre>Az alapítvány előélete
A Vasúttörténeti Alapítvány szellemi létrehozói több évtizedes vasúti munkájuk mellett rendszeresen foglalkoztak
a vasút történetének tanulmányozásával, munkaeszközök és dokumentumok gyűjtésével:

Felföldi Károly – a kecskeméti pft. vezetőjeként – a Cegléd-szegedi vasútvonal történeti kutatásait végezte az 1980-as években
és fő szervezője volt a 125 éves jubileum kecskeméti rendezvényeinek.

Nagy József – az egykori kisvasúti mozdonyvezető – alapító szervezője 1984-ben a Bugaci Kispöfögő Baráti körének,
a Kecskeméti Kisvasút Napjának, amit 25 éven át rendeztek meg. Vasúti gyűjteménye, miután egész lakását betöltötte, részben az alapítványban kapott méltó elhelyezést.

Csendom Lajosnak – mint az akkori kecskeméti vontatási főnöknek – nagy szerepe volt a 490.053 sz. mozdony és a kisvasút személy- és teherkocsi parkjának megóvásában.

Szabó Gyula – a szegedi Vasútigazgatóság vezetőjeként – 1982-ben a Pest-Zimonyi vasútvonal 100 éves jubileumi ünnepségének fő szervezője volt.
A Közművelődési Bizottság titkáraként kóstolt bele a megemlékezések és a méltó ünnepségek megszervezésébe.
